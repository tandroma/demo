int	ft_strcmp(char	*s1, char	*s2)
{
	int	i_s1;
	int	i_s2;

	i_s1 = 0;
	i_s2 = 0;
	while (s1[i_s1])
	{
		i_s1++;
	}
	while (s2[i_s2])
	{
		i_s2++;
	}
	if (i_s2 > i_s1)
		return (1);
	else if (i_s2 < i_s1)
		return (-1);
	else
		return (0);
}
